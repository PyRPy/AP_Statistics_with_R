
# Variance and standard deviation -----------------------------------------

# Today's topic

# let's get some numbers
myNumbers <- c(2, 4, 3, 8, 1, 11, 14, 0, 3, 12)

# we have 10 numbers, check it in environment on the right

# find the variance
var(myNumbers)

# find the sd
sd(myNumbers)

# how to find the relationship between variance and sd ?
# check this out :

sqrt(var(myNumbers))

sqrt(var(myNumbers)) == sd(myNumbers)

# we got 'true' , so it is equivalent between 'sqrt(var(myNumbers)) == sd(myNumbers)'

